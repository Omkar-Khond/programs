package Marvellous;

public class Arithmatic
{
    public int iValue1 = 0;
    public int iValue2 = 0;

   public Arithmatic(int A, int B)
    {
        iValue1 = A;
        iValue2 = B;

    }

   public int Addition()
    {
        return iValue1 + iValue2;
    }

    public int Subtract()
    {
        return iValue1 - iValue2;
    }
        

}